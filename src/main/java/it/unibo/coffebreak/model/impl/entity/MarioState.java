package it.unibo.coffebreak.model.impl.entity;

/**
 * Represents the different states that Mario can be in during the game.
*/
public enum MarioState {
   /**
    * The normal state of Mario.
    */
   NORMAL,

   /**
    * The state of Mario when he has a hammer.
    */
   WITHHAMMER,

   /**
    * The state of Mario when he is dead.
    */
   DEAD
}
